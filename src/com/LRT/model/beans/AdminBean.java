package com.LRT.model.beans;

public class AdminBean {
	int numberOfUsers;
	int numberOfCros;
	int hourlyRate;
	int numberOfFeedbacks;

	public int getNumberOfUsers() {
		return numberOfUsers;
	}

	public void setNumberOfUsers(int numberOfUsers) {
		this.numberOfUsers = numberOfUsers;
	}

	public int getNumberOfCros() {
		return numberOfCros;
	}

	public void setNumberOfCros(int numberOfCros) {
		this.numberOfCros = numberOfCros;
	}

	public int getHourlyRate() {
		return hourlyRate;
	}

	public void setHourlyRate(int hourlyRate) {
		this.hourlyRate = hourlyRate;
	}

	public int getNumberOfFeedbacks() {
		return numberOfFeedbacks;
	}

	public void setNumberOfFeedbacks(int numberOfFeedbacks) {
		this.numberOfFeedbacks = numberOfFeedbacks;
	}

}
